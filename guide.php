            <div id="race">
                <h2>Races</h2>
                <p>There are 11 official playable races in D&D 5e.</p>
                
                <h4>Dragonborn</h4>
                <p>
                    Essentially, they are their 4e counterparts with vaguer backgrounds, dragonborn are still pretty close to what they were. +2 Strength, +1 Charisma,
                    and choose one Chromatic or Metallic Dragon; they get a breath weapon shape, breath weapon damage, 
                    and damage resistance based on what they chose (cold for Silver Dragonborn, acid for Black, etc).
                </p>
                
                <h4>Dwarf</h4>
                <p>
                    <p>
                    Your standard dwarf. Short and stout, grumpy but loyal, love digging, and tough as an old boot. They get a +2 bonus to Constitution, have Darkvision,
                    protection against poison, training with axe and hammer weapons, training with several kinds of artisan's tools, the usual dwarven armored movement and stone knowledge.
                    They get two subraces; Hill and Mountain.
                    </p>
                    <p>
                    Hill dwarves are wiser (+1 Wisdom) and even tougher than regular dwarves, giving them extra maximum hit points equal to their character level.
                    Mountain dwarves are more warlike, getting +2 Strength and free proficiency with light armor and medium armor. The *only* subrace to offer two +2 bonuses now,
                    balanced by the fact that the armor training will almost certainly be completely redundant to any class that would realistically make use of those stats. However,
                    it's a good choice for a squishy character who wants melee a bit, like a blade-pact warlock.
                    </p>
                    <p>
                    Duergar, or "Gray Dwarves", appear in the Sword Coast Adventurer's Guide. In addition to the base dwarf stuff, they get a boost to Strength,
                    Superior Darkvision (so Darkvision 120 feet), Duergar Resilience (Advantage on saving throws against Charm, Illusion and Paralysis effects),
                    and the spell-like abilities of Enlarge/Reduce (3rd level) and Invisibility (5th level), both usable once per long rest. Uniquely, they recharge these spell-like abilities faster when in the Underdark,
                    allowing them to use these spells once per short rest down there. They also get Sunlight Sensitivity, though tweaked slightly;
                    in addition to taking disadvantage to attack rolls and Perception checks when they or their target is in direct sunlight, they can't use their spell-like abilities if they're in direct sunlight.
                    </p>
                </p>
                <h4>Elf</h4>
                <p>
                    <p>
                    Still pretty standard; graceful, eerie, beautiful. Grace translates to a +2 bonus to Dexterity, keen senses give them Darkvision and proficiency in Perception, they are resistant to charming and immune to sleep,
                    and they trance instead of sleeping. They get three subraces; high, wood and dark.
                    </p>
                    <p>
                    High elves are the magically adept elite. They get +1 Intelligence, proficiency with long & short swords and bows, an extra language, and the ability to cast one wizard cantrip of the player's choice.
                    </p>
                    <p>
                    Wood elves are the iconic forest-dwelling primal elves. +1 to Wisdom, same weapon proficiency as High Elves, even quicker (they have base speed 35 feet,
                    making them the fastest of the default races), and they're extra adept at using natural phenomena for hiding.
                    </p>
                    <p>
                    Dark elves have innate magic (Dancing Lights cantrip at level 1, Faerie Fire 1/day at level 3, Darkness 1/day at level 5), Superior Darkvision (Darkvision to 120 feet), +1 Charisma, proficiency with rapiers, shortswords and hand crossbows,
                    and are the only (sub)race in the corebook with any kind of racial penalty; they take disadvantage to attack rolls and Perception checks when they or their target is in direct sunlight.
                    </p>
                    <p>
                    The DMG-added Eladrin get the elf weapon proficiency (as per High/Wood Elves), +1 Intelligence, and Misty Step, like "Fey Step" from 4E.
                    Strangely, although the Sword Coast Adventurer's Guide contains rules for Half-Elves of Aquatic Elf ancestry, there's no rules for a pure-blooded Aquatic Elf,
                    but it's only logical that it'll come out in a following supplement or Unearthed Arcana. If it does, we at least know, from the way the other half-elf subraces worked, that it'll include a 30-ft swim speed.
                    </p>
                    
                </p>
                <h4>Gnome</h4>
                <p>
                    <p>
                    Crazy, hyper-energetic and insatiably curious, gnomes are also the only uncommon race in the corebook with full subraces, assuming the dragonborn's choice of dragon doesn't count.
                    +2 Intelligence, small-sized, Darkvision, and advantage to any saving throw against magic that relies on Intelligence, Wisdom or Charisma.
                    </p>
                    <p>
                        Forest Gnomes are the more mystical, nature-affiliated gnomes, gaining +1 Dexterity, having the minor illusion cantrip as a racial ability,
                        and being able to speak with any natural animal that is Small or smaller. 
                    </p>
                    <p>
                        Rock Gnomes are the iconic tinker gnomes, gaining +1 Constitution, being more adept at puzzling out magic items, alchemical objects and technological devices,
                        and starting the game with a set of tinker's tools that let them cobble together small, harmless gizmos like clockwork toys, fire starters and music boxes. 
                    </p>
                    <p>
                        Deep Gnomes got added by the Elemental Evil Player's Guide web-feature from the WoTC website. There was a printed reveal in the Sword Coast Adventurer's Guide, but the versions are absolutely identical. These are the "svirfneblin",
                        the Underdark-dwelling gnomes mentioned but mostly ignored in editions past. They get +1 Dexterity, Superior Darkvision (so they see in the dark 120ft instead of 60ft), and Stone Camouflage (advantage on Dexterity [Stealth] checks made in rocky terrain).
                        If Feats are allowed, they have a racial one called Svirfneblin Magic that lets them cast Nondetection (self-only) at will and Blindness/Deafness, Blur and Disguise Self once per long rest.
                        
                    </p>
                    
                </p>
                <h4>Half-Elf</h4>
                <p>
                    Half-Elves gain +2 Charisma, making them natural diplomats, but also get +1 to two other ability scores of their choice, are automatically proficient in two skills of their choice,
                    as well as retaining the darkvision and resistances to charming and sleep of their elven ancestors.
                    They can also grow beards, something that may have been in previous editions, but is directly addressed in this one. The best PHB race for any Cha-based class, due to their tremendous versatility.
                    <p>
                        The Sword Coast Adventurer's Guide includes rules for half-elf racial variants, allowing them to have sub-races like several other races. Basically, they can trade out their bonus skill proficiencies for the other elf races' bonuses.
                        Any kind of half-elf can trade for an upgrade to darkvision and proficiency in perception, half-high elves can gain a wizard cantrip, half-wood elves can gain a five foot speed boost or an improved ability to hide in the wild,
                        and both of the above can gain elf weapon training. Meanwhile, half-drow gain drow magic, while half-Aquatic Elves have a 30-ft swim speed. 
                        
                    </p>
                
                </p>
                <h4>Half-Orc</h4>
                <p>
                    Big scary bruisers, half-orcs get +2 strength and +1 constitution, have darkvision, are automatically proficient in the Intimidation skill, are harder to kill than other races,
                    and deal much nastier criticals with melee weapons. This effectively makes them the best barbarians in the game, and gives barbarian-like abilities to any other classes.
                </p>
                <h4>Halfling</h4>
                <p>
                    Small, cheerful, practical creatures, halflings try to make friends with anybody. They usually don't have any greater goal beyond a simple, pleasant life. They get +2 Dexterity,
                    they're Small sized, their Lucky trait lets them reroll various results of 1,
                    they're resistant to fear effects and they can move through spaces occupied by creatures that are Medium-sized or bigger. Their two subraces are lightfoot and stout.
                    
                    <p>
                        Lightfoot halflings are sneaky even by Halfling standards, able to use Medium-sized or bigger creatures to hide behind and gaining +1 Charisma.
                    </p>
                    <p>
                        Stout halflings are rumored to have dwarf blood, and so they get +1 Constitution and identical poison protection.
                    </p>
                    <p>
                        The Sword Coast Adventurer's Guide includes the ghostwise halfing subrace, which gets +1 to Wisdom and the ability to telepathically communicate with one nearby creature at a time.
                    </p>
                    
                </p>
                <h4>Human</h4>
                <p>
                    Humans are the versatile race once again. Either they get a +1 bonus to all ability scores (which is better than any previous edition of the game has trained you to believe,
                    but still kinda bland), or they get +1 to any two ability scores they want, a free skill proficiency, and a free feat.
                </p>
                <h4>Tiefling</h4>
                <p>
                    Tiefling with a unified (if still very variable) appearance and a tiefling racial backlore as "descendants of a cursed empire" rather than "spawn of a human and a fiend".
                    +1 Intelligence, +2 Charisma, resistant to fire, darkvision and "Infernal Legacy",
                    which gives them three warlock spells as racial abilities; the Thaumaturgy cantrip (level 1), Hellish Rebuke (1/day at level 3) and Darkness (1/day at level 5).
                </p>
                <h4>Gensai</h4>
                <p>
                    Part of the Elemental Evil Player's Guide web-feature from the WoTC website, they were the only race from it to make it into the official Prices of Elemental Evil adventure,
                    which keeps them safe from DMs who insist "it's not in print, so it's not official". Only four varieties this time; Earth, Air, Water and Fire.
                    All genasi get +2 Constitution and racial magic (usually of the cantrip and/or "once per long rest" spells variety) determined by their subrace, which uses their Constitution.
                    <p>
                        
                        
                    </p>
                    <p>
                        Air genasi gain +1 Dexterity, have Unending Breath (can hold their breaths indefinitely when not incapacitated) and can cast levitate.
                    </p>
                    <p>
                        Earth genasi gain +1 Strength, ignore movement penalties from earth/stone-based difficult terrain, and can cast pass without trace.
                    </p>
                    <p>
                        Fire genasi gain +1 Intelligence, darkvision, fire resistance, and can cast the produce flame cantrip, plus burning hands at level 3.
                    </p>
                    <p>
                        Water genasi gain +1 Wisdom, can breathe water and air, have acid resistance and a 30 ft swim speed, and get the Shape Water cantrip, plus create or destroy water at level 3.
                    </p>
                </p>
                <h4>Goliath</h4>
                <p>
                    Yet another big bruiser race, goliaths get +2 Strength, +1 Constitution, proficiency in athletics, the unique ability to use a reaction upon taking damage to roll a D12 and reduce that damage by [result + Constitution modifier] once per short rest,
                    count as one size larger for carrying, dragging,
                    pushing and lifting and are automatically acclimated to high altitude and naturally adapted to cold climates if your DM's using those rules.
                </p>
                
