                <h2>Core Stats</h2>
                <p>
                    <h5>SIX ABILITIES PROVIDE A QUICK DESCRIPTION</h5> 
                    of every creature's physical and mental
                    characteristics:
                    <p>&bull; Strength, measuring physical power</p>
                    <p>&bull; Dexterity, measuring agility</p>
                    <p>&bull; Constitution, measuring endurance</p>
                    <p>&bull; Intelligence, measuring reasoning and memory</p>
                    <p>&bull; Wisdom, measuring perception and insight</p>
                    <p>&bull; Charisma, measuring force of personality</p>
                    
                    Is a character muscle-bound and insightful? Brilliant and charming? Nimble and hardy? Ability scores
                    define these qualities a creature's assets as well as weaknesses.
                    The three main rolls of the game the ability check, the saving throw, and the attack roll rely on the six
                    ability scores. The book's introduction describes the basic rule behind these rolls: roll a d20, add an ability
                    modifier derived from one of the six ability scores, and compare the total to a target number.
                    
                    
                </p>
