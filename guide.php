                <h2>Info</h2>
                <p>
                        The Dungeons & Dragons role playing game is about storytelling in worlds of swords and sorcery. It shares elements 
                        with childhood games of make-believe. Like those games, D&D is driven by imagination. It's about picturing the towering castle 
                        beneath the stormy night sky and imagining how a fantasy adventurer might react to the challenges that scene presents.
                        Unlike a game of make-believe, D&D gives structure to the stories, a way of determining the consequences
                        of the adventurers' action. Players roll dice to resolve whether their attacks hit or miss or whether their adventurers
                        can scale a cliff, roll away from the strike of a magical lightning bolt, or pull off some other dangerous
                        task. Anything is possible, but the dice make some outcomes more probable than others.
                        In the Dungeons & Dragons game, each player
                        creates an adventurer (also called a character) and teams up with other adventurers (played by friends). Working together, the group might explore a dark dungeon,
                        a ruined city, a haunted castle, a lost temple deep in a jungle, or a lava-filled cavern beneath a mysterious
                        mountain. The adventurers can solve puzzles, talk with other characters, battle fantastic monsters, and discover
                        fabulous magic items and other treasure.
                        One player, however, takes on the role of the Dungeon Master (DM), the game's lead storyteller and referee.
                        The DM creates adventures for the characters, who navigate its hazards and decide which paths to explore. The
                        DM might describe the entrance to Castle Ravenloft, and the players decide what they want their adventurers
                        to do. Will they walk across the dangerously weathered drawbridge? Tie themselves together with rope to minimize the chance that som eone will fall if the drawbridge
                        gives way? Or cast a spell to carry them over the chasm? Then the DM determines the results of the adventurers'
                        actions and narrates what they experience. Because the DM can improvise to react to anything the players attempt, D&D is infinitely flexible, and each adventure
                        can be exciting and unexpected. The game has no real end; when one story or quest wraps up, another one can begin, creating an ongoing
                        story called a campaign. Many people who play the game keep their campaigns going for months or years, meeting with their friends every week or so to pick
                        up the story where they left off. The adventurers grow in might as the campaign continues. Each monster defeated, each adventure completed, and each treasure
                        recovered not only adds to the continuing story, but also earns the adventurers new capabilities. This increase in power is reflected by an adventurer's level.
                        There's no winning and losing in the Dungeons & Dragons game at least, not the way those terms are usually understood. Together, the DM and the players
                        create an exciting story of bold adventurers who confront
                        deadly perils. Sometimes an adventurer might come to a grisly end, torn apart by ferocious monsters or done in by a nefarious villain.
                        Even so, the other adventurers can search for powerful magic to revive their fallen comrade, or the player might choose to create a new character to
                        carry on. The group might fail to complete an adventure successfully, but if everyone had a good time and created a memorable story, they all win.

                </p>
                
